package com.unicorn;

public class Location {

    private int x;
    private int y;

    public Location(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return x;
    }

    public void setLocation(int x, int y) {
        this.x = x;
        this.y = x;
    }

}
